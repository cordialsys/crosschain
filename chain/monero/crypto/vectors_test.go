// derive_public_key vectors from monero-project/tests/crypto/tests.txt
// Format: derivation, output_index, base_public_key -> derived_public_key
func TestDerivePublicKey(t *testing.T) {
	vectors := []struct {
		derivation string
		index      uint64
		base       string
		expected   string
	}{
		{"ca780b065e48091d910de90bcab2411db3d1a845e6d95cfd556af4138504c737", 217407, "6d9dd2068b9d6d643b407e360dfc5eb7a1f628fe2de8112a9e5731e8b3680c39", "d48008aff5f27d8fcdc2a3bf814ed3505530f598075f3bf7e868fea696b109f6"},
		{"13bb0039172efee53059c7a973dc5f6f3c0a07611ebb0f5609cd833d5d25846c", 1, "5ca5429e836cd4172b7427ca8dc639f39c299f1b8e0d00f9d3f9a5bb2e49251a", "52e0a76a5785d12737dba717fd6c90e0e7d7a1a6c758543758abe578793c7a52"},
		{"fc9f87293569070b7e2e1be48e6ffcdfef370a728d4c01159b5b7b9783e0fa0f", 1499890121, "2c887eb3a891f60d9382b9a368f7d8bbd91fc8742dfe1054d1999e9f928e399b", "678c62af985543c426e90db94de447219ac24d8f3f44652003fe2b70bef54092"},
		{"b7884ba954056a2c33f2da970e4b14de9a9fee254d569e34c68c43a1835234c1", 771, "fd90bc87b73dfcc94ddd5e1b5090ee6537b4ccbe1fade2b542d9073f980a1db4", "dc9700bfa55175403c5c2db22d2685252504e4379e4fc169fe52e1bb8b65e869"},
		{"75c4b56550636fa58f837511c8054106633b577654e80f766cc608aaefb67dd4", 7040, "6b7a50dc0993b9d7c96fd028153cf9e8abb150e461b25c15ba2c437e52aefcbe", "ff8bc368609807c9d3da866ac660d8f051b6f93b2709fb5dc303e5eeca4300bd"},
	}

	for _, v := range vectors {
		derivation := hexDec(t, v.derivation)
		base := hexDec(t, v.base)

		// scalar = H_s(derivation || varint(index))
		scalar, err := DerivationToScalar(derivation, v.index)
		require.NoError(t, err)

		// derived = scalar*G + base
		sScalar, _ := edwards25519.NewScalar().SetCanonicalBytes(scalar)
		sG := edwards25519.NewGeneratorPoint().ScalarBaseMult(sScalar)
		basePoint, _ := edwards25519.NewIdentityPoint().SetBytes(base)
		derived := edwards25519.NewIdentityPoint().Add(sG, basePoint)

		require.Equal(t, v.expected, hex.EncodeToString(derived.Bytes()),
			"derive_public_key(derivation=%s, idx=%d)", v.derivation[:16], v.index)
	}
}

