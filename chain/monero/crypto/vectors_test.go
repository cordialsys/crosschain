// Transaction hash test vector from confirmed testnet tx.
// Verifies the three-hash structure: H(H(prefix) || H(rct_base) || H(rct_prunable))
func TestTransactionHashFromBlob(t *testing.T) {
	// Confirmed testnet tx e2ead289... (1527 bytes)
	// Component hashes extracted by parsing the blob and verified by both Go and C++.

	// TX hash = H(prefix_hash || rct_base_hash || rct_prunable_hash)
	prefixHash := hexDec(t, "7d0ca1767ed06a59ac9f2203381764bbb566b6505f674f7da30d3b2b23e5685a")
	rctBaseHash := hexDec(t, "9628ee7ac4d2cfabad6ce5eaa40a51eaea045c28bce6174d54e79805dca73e14")
	rctPrunableHash := hexDec(t, "d01134cd3732fc96cdb30284da825b6a7d6d3bceea58d9b20dc6b39a3fd198d4")

	combined := make([]byte, 0, 96)
	combined = append(combined, prefixHash...)
	combined = append(combined, rctBaseHash...)
	combined = append(combined, rctPrunableHash...)
	txHash := Keccak256(combined)

	require.Equal(t, "e2ead28997ea7e5b84ca198a6021a995bc28e743a0fdacdb89cb1ae2d7e1198b",
		hex.EncodeToString(txHash))

	// CLSAG message = H(prefix_hash || rct_base_hash || bp_kv_hash)
	// Note: bp_kv_hash != rct_prunable_hash (prunable includes CLSAG + pseudoOuts)
	bpKvHash := hexDec(t, "4e48d0e88081c5bc25991539a66e8d8171df8f53cfbda65613145b9ff0c1aa84")
	combined2 := make([]byte, 0, 96)
	combined2 = append(combined2, prefixHash...)
	combined2 = append(combined2, rctBaseHash...)
	combined2 = append(combined2, bpKvHash...)
	clsagMessage := Keccak256(combined2)

	require.Equal(t, "75d15480f861aa29fbd0971f1ca1720a17b4dd6f75476ca34b1e62fa5ede7fb1",
		hex.EncodeToString(clsagMessage))
}

